/**
 * Integration tests for the clojure.test → vitest failure bridge.
 *
 * These tests exercise the ACTUAL session execution — not just code generation
 * string-matching. They verify that:
 *
 *   - Failing `is` assertions inside both sync and async deftests are captured
 *     in the `__vt_failures` atom (so the generated test wrapper can throw).
 *   - Multiple failures from a single deftest are all captured.
 *   - Uncaught `throw` forms propagate as rejected promises and make
 *     `evaluateAsync` throw (so vitest marks the test as errored, not just failed).
 *   - Passing assertions leave `__vt_failures` empty.
 *
 * The session setup here mirrors exactly what `generateTestModuleCode` emits,
 * so regressions in either the codegen or the evaluator are caught here.
 */

import { describe, it, expect } from 'vitest'
import { createSession, cljToJs } from '../../core/index'
import type { Session } from '../../core/index'

// ---------------------------------------------------------------------------
// Helper — mirrors the session setup generated by generateTestModuleCode
// ---------------------------------------------------------------------------

function makeTestSession(): Session {
  const session = createSession({ output: () => {} })

  // Load clojure.test and refer the test macros into user namespace —
  // same effect as (:require [clojure.test :refer [deftest is testing are]])
  // in a .clj ns form.
  session.evaluate("(require '[clojure.test :refer [deftest is testing are]])")
  session.evaluate('(def __vt_failures (atom []))')

  // :fail — same multiline string as in generateTestModuleCode
  session.evaluate(
    '(defmethod clojure.test/report :fail [m]' +
    '  (swap! __vt_failures conj' +
    '    (str' +
    '      (when (:message m) (str (:message m) "\\n"))' +
    '      "expected: " (pr-str (:expected m)) "\\n"' +
    '      "  actual: " (pr-str (:actual m)))))'
  )
  // :error — captures the actual value (exception or error map)
  session.evaluate(
    '(defmethod clojure.test/report :error [m]' +
    '  (swap! __vt_failures conj (str "error: " (pr-str (:actual m)))))'
  )
  // silence all other report methods (same as generated code)
  session.evaluate('(defmethod clojure.test/report :pass [_] nil)')
  session.evaluate('(defmethod clojure.test/report :begin-test-var [_] nil)')
  session.evaluate('(defmethod clojure.test/report :end-test-var [_] nil)')
  session.evaluate('(defmethod clojure.test/report :begin-test-ns [_] nil)')
  session.evaluate('(defmethod clojure.test/report :end-test-ns [_] nil)')
  session.evaluate('(defmethod clojure.test/report :summary [_] nil)')

  return session
}

/** Run one deftest (by name string) and return the collected failure strings. */
async function runDeftest(session: Session, testName: string): Promise<unknown[]> {
  session.evaluate('(reset! __vt_failures [])')
  await session.evaluateAsync(`(${testName})`)
  const failures = cljToJs(session.evaluate('@__vt_failures'), session)
  return failures as unknown[]
}

// ---------------------------------------------------------------------------
// Async deftest — failure paths
// ---------------------------------------------------------------------------

describe('async deftest — failure paths', () => {
  it('captures a failing is assertion inside (async ...)', async () => {
    const session = makeTestSession()
    session.evaluate('(deftest t (async (let [x @(promise-of 1)] (is (= x 99)))))')

    const failures = await runDeftest(session, 't')

    expect(failures.length).toBe(1)
    // is reports (:expected m) as the form, (:actual m) as the evaluated boolean
    expect(failures[0]).toContain('expected: (= x 99)')
    expect(failures[0]).toContain('actual: false')
  })

  it('captures multiple failing assertions from one async deftest', async () => {
    const session = makeTestSession()
    session.evaluate(
      '(deftest t' +
      '  (async' +
      '    (let [x @(promise-of 1)]' +
      '      (is (= x 10))' +
      '      (is (= x 20))' +
      '      (is (= x 30)))))'
    )

    const failures = await runDeftest(session, 't')

    // All three assertions run even after the first fails (is does not throw)
    expect(failures.length).toBe(3)
  })

  it('leaves __vt_failures empty when all async assertions pass', async () => {
    const session = makeTestSession()
    session.evaluate('(deftest t (async (let [x @(promise-of 42)] (is (= x 42)))))')

    const failures = await runDeftest(session, 't')

    expect(failures).toEqual([])
  })

  it('propagates a thrown exception from inside (async ...) as a rejected promise', async () => {
    const session = makeTestSession()
    // deftest body throws directly inside the async block
    session.evaluate('(deftest t (async (throw "async-boom")))')
    session.evaluate('(reset! __vt_failures [])')

    // evaluateAsync must reject — the generated async test callback would then
    // propagate the rejection to the test runner (vitest / bun:test).
    await expect(session.evaluateAsync('(t)')).rejects.toThrow()
  })

  it('does not swallow the throw — __vt_failures stays empty when an exception is thrown', async () => {
    const session = makeTestSession()
    session.evaluate('(deftest t (async (throw "should-not-be-swallowed")))')
    session.evaluate('(reset! __vt_failures [])')

    try {
      await session.evaluateAsync('(t)')
    } catch {
      // expected — swallow here so we can inspect the atom
    }

    // The throw propagated as a rejection, not through the :error report method
    const failures = cljToJs(session.evaluate('@__vt_failures'), session)
    expect(failures).toEqual([])
  })
})

