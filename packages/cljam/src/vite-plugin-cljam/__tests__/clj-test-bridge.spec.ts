describe('sync deftest — failure paths', () => {
  it('captures a failing is assertion in a sync deftest', async () => {
    const session = makeTestSession()
    session.evaluate('(deftest t (is (= 1 99)))')

    const failures = await runDeftest(session, 't')

    expect(failures.length).toBe(1)
    expect(failures[0]).toContain('expected: (= 1 99)')
    expect(failures[0]).toContain('actual: false')
  })

  it('captures multiple failing assertions from one sync deftest', async () => {
    const session = makeTestSession()
    session.evaluate('(deftest t (is (= 1 10)) (is (= 2 20)) (is (= 3 30)))')

    const failures = await runDeftest(session, 't')

    expect(failures.length).toBe(3)
  })

  it('leaves __vt_failures empty when all sync assertions pass', async () => {
    const session = makeTestSession()
    session.evaluate('(deftest t (is (= 1 1)) (is (string? "ok")))')

    const failures = await runDeftest(session, 't')

    expect(failures).toEqual([])
  })

  it('propagates a thrown exception from a sync deftest as a rejected promise', async () => {
    const session = makeTestSession()
    session.evaluate('(deftest t (throw "sync-boom"))')
    session.evaluate('(reset! __vt_failures [])')

    await expect(session.evaluateAsync('(t)')).rejects.toThrow()
  })
})

