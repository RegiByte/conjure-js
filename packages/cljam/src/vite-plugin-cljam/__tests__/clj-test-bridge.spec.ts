describe('async deftest — testing context', () => {
  it('captures failures with testing context labels inside (async ...) — lifted @ pattern', async () => {
    const session = makeTestSession()
    // The lifted-@ pattern: deref is in the outer (async let ...), not inside the
    // (testing ...) body. Works before and after the binding-async fix.
    session.evaluate(
      '(deftest t' +
      '  (async' +
      '    (let [x @(promise-of 1)]' +
      '      (testing "my context"' +
      '        (is (= x 99))))))'
    )

    const failures = await runDeftest(session, 't')

    expect(failures.length).toBe(1)
    expect(failures[0]).toContain('expected: (= x 99)')
    expect(failures[0]).toContain('actual: false')
  })
})

// ---------------------------------------------------------------------------
// Async deftest — binding inside async
// (binding [...] body) inside (async ...) must run the body
// synchronously — making @deref inside any binding-body fail. That included
// (testing ...) which expands to a (binding [*testing-contexts* ...] (thunk)).
// ---------------------------------------------------------------------------

describe('async deftest — binding inside async', () => {
  it('(binding [...] body) with @deref inside body works', async () => {
    const session = makeTestSession()
    session.evaluate('(def ^:dynamic *factor* 1)')
    session.evaluate(
      '(deftest t' +
      '  (async' +
      '    (binding [*factor* 10]' +
      '      (let [x @(promise-of 5)]' +
      '        (is (= (* x *factor*) 50))))))'
    )

    const failures = await runDeftest(session, 't')

    expect(failures).toEqual([])
  })

  it('captures a failure from inside (binding [...] @deref-body)', async () => {
    const session = makeTestSession()
    session.evaluate('(def ^:dynamic *factor* 1)')
    session.evaluate(
      '(deftest t' +
      '  (async' +
      '    (binding [*factor* 10]' +
      '      (let [x @(promise-of 5)]' +
      '        (is (= (* x *factor*) 999))))))'
    )

    const failures = await runDeftest(session, 't')

    expect(failures.length).toBe(1)
    expect(failures[0]).toContain('actual: false')
  })

  it('binding is popped correctly after async body — var returns to prior value', async () => {
    const session = makeTestSession()
    session.evaluate('(def ^:dynamic *factor* 1)')
    // Run the binding test once to push/pop
    session.evaluate(
      '(deftest t' +
      '  (async' +
      '    (binding [*factor* 99]' +
      '      (let [_ @(promise-of nil)]' +
      '        (is (= *factor* 99))))))'
    )
    await runDeftest(session, 't')
    // After the test, the binding must have been popped — *factor* should be 1 again
    const val = session.evaluate('*factor*')
    const { cljToJs } = await import('../../core/index')
    expect(cljToJs(val, session)).toBe(1)
  })

  it('(testing "..." (let [x @...] ...)) — @deref inside testing body now works', async () => {
    const session = makeTestSession()
    // testing expands to (with-testing-context* str (fn [] body)), which uses
    // (binding [*testing-contexts* ...] (thunk)). With the binding-async fix,
    // the thunk body runs async, so @deref inside it works directly.
    session.evaluate(
      '(deftest t' +
      '  (async' +
      '    (testing "inner context"' +
      '      (let [x @(promise-of 42)]' +
      '        (is (= x 42))))))'
    )

    const failures = await runDeftest(session, 't')

    expect(failures).toEqual([])
  })

  it('captures failure from @deref inside (testing ...) body', async () => {
    const session = makeTestSession()
    session.evaluate(
      '(deftest t' +
      '  (async' +
      '    (testing "failing context"' +
      '      (let [x @(promise-of 1)]' +
      '        (is (= x 999))))))'
    )

    const failures = await runDeftest(session, 't')

    expect(failures.length).toBe(1)
    expect(failures[0]).toContain('expected: (= x 999)')
    expect(failures[0]).toContain('actual: false')
  })
})

// ---------------------------------------------------------------------------
// use-fixtures :each — codegen integration
//
// These tests verify that the __vt_each_fixture mechanism produced by
// generateTestModuleCode correctly applies :each fixtures around test calls.
// The session setup mirrors exactly what the codegen emits: after loading
// the test source, `__vt_each_fixture` is defined via join-fixtures, and
// each test invocation calls (__vt_each_fixture (fn [] (test-name))).
// ---------------------------------------------------------------------------

/** Mirror of the fixture setup line emitted by generateTestModuleCode. */
function installEachFixture(session: Session, nsName: string): void {
  const fixtureSetup = `(def __vt_each_fixture (clojure.test/join-fixtures (get @clojure.test/fixture-registry ["${nsName}" :each] [])))`
  session.evaluate(fixtureSetup)
}

/** Run one deftest wrapped by __vt_each_fixture (mirrors generated test() callback). */
async function runDeftestWithFixture(session: Session, testName: string): Promise<unknown[]> {
  session.evaluate('(reset! __vt_failures [])')
  await session.evaluateAsync(`(__vt_each_fixture (fn [] (${testName})))`)
  const failures = cljToJs(session.evaluate('@__vt_failures'), session)
  return failures as unknown[]
}

describe('use-fixtures :each — codegen integration', () => {
  it('applies fixture setup before the test body', async () => {
    const session = makeTestSession()
    session.evaluate(`
      (require '[clojure.test :refer [deftest is use-fixtures]])
      (def log (atom []))
      (defn fx [f] (swap! log conj :setup) (f) (swap! log conj :teardown))
      (use-fixtures :each fx)
      (deftest t (swap! log conj :test))
    `)
    installEachFixture(session, 'user')

    await runDeftestWithFixture(session, 't')

    const log = cljToJs(session.evaluate('@log'), session) as unknown[]
    // cljToJs converts keywords to plain strings (no colon prefix)
    expect(log).toEqual(['setup', 'test', 'teardown'])
  })

  it('applies fixture teardown after the test body', async () => {
    const session = makeTestSession()
    session.evaluate(`
      (require '[clojure.test :refer [deftest is use-fixtures]])
      (def log (atom []))
      (defn fx [f] (f) (swap! log conj :teardown))
      (use-fixtures :each fx)
      (deftest t (swap! log conj :test))
    `)
    installEachFixture(session, 'user')

    await runDeftestWithFixture(session, 't')

    const log = cljToJs(session.evaluate('@log'), session) as unknown[]
    expect(log[log.length - 1]).toBe('teardown')
  })

  it('fixture teardown runs even after a failing assertion', async () => {
    const session = makeTestSession()
    session.evaluate(`
      (require '[clojure.test :refer [deftest is use-fixtures]])
      (def teardown-ran (atom false))
      (defn fx [f] (f) (reset! teardown-ran true))
      (use-fixtures :each fx)
      (deftest t (is (= 1 99)))
    `)
    installEachFixture(session, 'user')

    const failures = await runDeftestWithFixture(session, 't')

    // Failure captured AND teardown ran
    expect(failures.length).toBe(1)
    expect(cljToJs(session.evaluate('@teardown-ran'), session)).toBe(true)
  })

  it('multiple :each fixtures run in correct order (outer→inner setup, inner→outer teardown)', async () => {
    const session = makeTestSession()
    session.evaluate(`
      (require '[clojure.test :refer [deftest is use-fixtures]])
      (def log (atom []))
      (defn f1 [g] (swap! log conj :f1-in) (g) (swap! log conj :f1-out))
      (defn f2 [g] (swap! log conj :f2-in) (g) (swap! log conj :f2-out))
      (use-fixtures :each f1 f2)
      (deftest t (swap! log conj :test))
    `)
    installEachFixture(session, 'user')

    await runDeftestWithFixture(session, 't')

    const log = cljToJs(session.evaluate('@log'), session) as unknown[]
    expect(log).toEqual(['f1-in', 'f2-in', 'test', 'f2-out', 'f1-out'])
  })

  it('fixture side effects are visible inside the test body', async () => {
    const session = makeTestSession()
    session.evaluate(`
      (require '[clojure.test :refer [deftest is use-fixtures]])
      (def state (atom nil))
      (defn fx [f] (reset! state :ready) (f))
      (use-fixtures :each fx)
      (deftest t (is (= @state :ready)))
    `)
    installEachFixture(session, 'user')

    const failures = await runDeftestWithFixture(session, 't')

    // Zero failures → the (is (= @state :ready)) assertion passed
    expect(failures).toEqual([])
  })
})
